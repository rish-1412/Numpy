# Array filled with zeros

import numpy as np
a_zero = np.zeros(4) # creates a  1 row and 4 column array with zeros
print(a_zero)

b_zero = np.zeros((3,4)) # creates a 3 row and 4 column array with zeros
print(b_zero)

# Array filled with ones

a_ones = np.ones((5,4)) # creates a 5 row and 4 column array with ones
print(a_ones)

# Empty array
a_empty = np.empty(4)
print(a_empty)